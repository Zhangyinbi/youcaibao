public class Materials {
    private String matId;
    private String count;

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getMatId() {
        return matId;
    }

    public void setMatId(String matId) {
        this.matId = matId;
    }

    @Override
    public String toString() {
        return "{" +
                "matId='" + matId + '\'' +
                ", count='" + count + '\'' +
                '}';
    }
}
